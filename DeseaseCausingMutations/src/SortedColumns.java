public class SortedColumns {

    private char[] cca;

    private String[] cs;

    public SortedColumns(char[] cca, String[] cs) {
        super();
        this.cca = cca;
        this.cs = cs;
    }

    public char[] getCca() {
        return cca;
    }

    public String[] getCs() {
        return cs;
    }

}
